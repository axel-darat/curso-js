let a = 5;
let b = 7;

console.log('adición', a + b);
console.log('resta', a - b);
console.log('multiplicación', a * b);
console.log('división', a / b);
console.log('módulo', a % b);
console.log('potencia', a ** b);

// incrementar
console.log(++a);
console.log(a++);
console.log(a);

// decrementar
console.log(--a);
console.log(a--);
console.log(a);
