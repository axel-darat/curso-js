let a = 5;
let b = 7;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b); // módulo (lo que sobra de una división)
console.log(a ** b); // potencia

// INCREMENTAR
console.log(++a);
console.log(a++);

// DECREMENTAR
console.log(--a);
console.log(a--);
console.log(a);